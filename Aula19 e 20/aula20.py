primeiro_valor = input('Digite o primeiro valor: ')
segundo_valor = input('Digite o segundo valor: ')

if primeiro_valor > segundo_valor :
        print(f'O primeiro valor "{primeiro_valor}" e maior que o segundo valor "{segundo_valor}"')
elif primeiro_valor == segundo_valor :
        print(f'O primeiro valor "{primeiro_valor}" e o segundo valor "{segundo_valor}" sao iguais!')
elif primeiro_valor < segundo_valor :
        print(f'O primeiro valor "{primeiro_valor}" e menor: "{segundo_valor}"')